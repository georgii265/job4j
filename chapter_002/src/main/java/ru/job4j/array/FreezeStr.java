/**
 * Даны две строки.
 * Нужно проверить, что вторая строка получилась методом перестановок символов в первой строке.
 */
public class FreezeStr {

    public static boolean eq(String left, String right) {
        if (left.length() != right.length()) {
           return false;
        }
        return false;
    }
}